#include <iostream>
#include <cmath>
#include <iomanip>
using namespace std;

/*  Program Description:
    This program will get two floating point inputs for the two sides of right triangle. 
    From that, it willl compute and display the length of the hypotenuse.
*/

int main() {
    
    //Variables
    
    double side1;		// the length of side1 of the triangle
    double side2;		// the length of side2 of the triangle
    
    // Set output to two decimal places
    
    cout << fixed << showpoint << setprecision(2); 	// sets precision to 2 decimal places

    //Describes the process to the user
    
    cout << "This program will prompt the user for the length of two sides " <<endl;
    cout << "of a right triangle, from those lengths, the program will compute " <<endl;
    cout << "and display the length of the hypotenuse." <<endl <<endl;

    //Prompts the user and reads the inputs

    cout << "Enter the length of the first side: "; cin >> side1; 	//prompts for side 1
    cout << "Enter the length of the second side: "; cin >> side2; 	// prompts for side 2

    //Calculate and report
    
    //calcuates lenght of hypotenuse by using hypotenuse equals the square root
    //of the two sides squared
    cout << "\nLength of the hypotenuse: " << sqrt( pow(side1, 2) + pow(side2, 2) );
    cout << endl << endl;

    return 0; //end program

} // end main
